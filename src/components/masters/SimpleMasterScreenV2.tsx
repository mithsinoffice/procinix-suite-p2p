function FormView({
  config,
  apiPath,
  codeKey,
  nameKey,
  mode,
  initial,
  onBack,
  onSave,
  onDeactivate,
  onApprovalAction,
}: FormViewProps) {
  const Icon = config.icon;
  const isView = mode === 'view';
  const isEdit = mode === 'edit';
  const isAdd = mode === 'add';
  const readOnly = isView;

  const [form, setForm] = useState<MasterV2Record>(initial);
  const [touched, setTouched] = useState<Set<string>>(new Set());
  const [errors, setErrors] = useState<Record<string, string>>({});
  const [saveState, setSaveState] = useState<SaveState>('idle');
  const [lastSavedAt, setLastSavedAt] = useState<Date | null>(
    initial.updatedAt ? new Date(String(initial.updatedAt)) : null
  );
  const [toasts, setToasts] = useState<Toast[]>([]);

  const debounceRef = useRef<ReturnType<typeof setTimeout> | null>(null);

  const allFields = useMemo(
    () => config.formSections.flatMap((s) => s.fields),
    [config.formSections]
  );

  // ── Toast helpers ────────────────────────────────────────────────────────
  const pushToast = useCallback((tone: Toast['tone'], message: string) => {
    setToasts((cur) => {
      const next = [
        ...cur,
        { id: `t-${Date.now()}-${Math.random().toString(36).slice(2)}`, tone, message },
      ];
      return next.slice(-3);
    });
  }, []);
  const dismissToast = useCallback((id: string) => {
    setToasts((cur) => cur.filter((t) => t.id !== id));
  }, []);
  // Auto-dismiss success toasts after 3s
  useEffect(() => {
    const timers = toasts
      .filter((t) => t.tone === 'success')
      .map((t) =>
        setTimeout(() => {
          setToasts((cur) => cur.filter((x) => x.id !== t.id));
        }, 3000)
      );
    return () => timers.forEach(clearTimeout);
  }, [toasts]);

  // ── Validation ───────────────────────────────────────────────────────────
  const validateField = useCallback(
    (field: MasterV2Field, value: unknown, fullForm: MasterV2Record): string | null => {
      if (field.autoGenerated) return null;
      if (field.required) {
        if (value == null || (typeof value === 'string' && value.trim() === '')) {
          return `${field.label} is required`;
        }
      }
      if (field.validate) return field.validate(value, fullForm);
      return null;
    },
    []
  );

  const validateAll = useCallback((): Record<string, string> => {
    const next: Record<string, string> = {};
    for (const field of allFields) {
      const v = readField(form, field);
      const err = validateField(field, v, form);
      if (err) next[field.key] = err;
    }
    // Code is always required (universal master rule); Name is required
    // unless the master suppresses the universal Name field — in that
    // case the master has its own first/last (or similar) split that
    // V2 has no opinion about, beyond synthesising `recordName` via
    // `config.computeRecordName` at save time for downstream consumers.
    if (!String(form.recordCode ?? form.code ?? '').trim()) {
      next['__code'] = 'Code is required';
    }
    if (!config.suppressUniversalName && !String(form.recordName ?? form.name ?? '').trim()) {
      next['__name'] = 'Name is required';
    }
    return next;
  }, [allFields, form, validateField, config.suppressUniversalName]);

  // ── Field mutation ───────────────────────────────────────────────────────
  const writeField = (field: MasterV2Field, value: unknown) => {
    setForm((prev) => {
      const cast =
        field.type === 'number' || field.type === 'currency'
          ? value === '' || value == null
            ? null
            : Number(value)
          : value;
      if (field.fromPayload) {
        const payload = { ...((prev.payload as Record<string, unknown>) ?? {}) };
        payload[field.key] = cast;
        return { ...prev, payload };
      }
      return { ...prev, [field.key]: cast };
    });
  };

  const writeTop = (key: string, value: unknown) => {
    setForm((prev) => ({ ...prev, [key]: value }));
  };

  const onFieldBlur = (field: MasterV2Field) => {
    setTouched((cur) => {
      const next = new Set(cur);
      next.add(field.key);
      return next;
    });
    const err = validateField(field, readField(form, field), form);
    setErrors((cur) => {
      const next = { ...cur };
      if (err) next[field.key] = err;
      else delete next[field.key];
      return next;
    });
    // Auto-save: only in edit mode, only if no validation errors, debounced
    if (isEdit && Object.keys(errors).length === 0 && !err) {
      if (debounceRef.current) clearTimeout(debounceRef.current);
      debounceRef.current = setTimeout(() => {
        setSaveState('saving');
        const saved = onSave(form);
        setLastSavedAt(new Date());
        setSaveState('saved');
        setTimeout(() => setSaveState('idle'), 1200);
        setForm(saved);
      }, 1500);
    }
  };

  // ── Submit handlers ──────────────────────────────────────────────────────
  const submit = (activate: boolean) => {
    const validation = validateAll();
    setErrors(validation);
    if (Object.keys(validation).length > 0) {
      pushToast(
        'error',
        `Fix ${Object.keys(validation).length} validation error${Object.keys(validation).length === 1 ? '' : 's'} before saving.`
      );
      return;
    }
    setSaveState('saving');
    // When this master requires approval, activation goes via the approval
    // queue — flip status to Pending Approval instead of Approved/Active.
    const targetApproval = activate
      ? config.requiresApproval
        ? 'Pending Approval'
        : 'Approved'
      : 'Draft';
    const computedName = config.computeRecordName?.(form);
    const next: MasterV2Record = {
      ...form,
      status: activate ? 'Active' : (form.status ?? 'Active'),
      approvalStatus: targetApproval,
      ...(computedName
        ? { recordName: computedName, name: computedName, [nameKey]: computedName }
        : {}),
    };
    const saved = onSave(next);
    setForm(saved);
    setLastSavedAt(new Date());
    setSaveState('saved');
    pushToast(
      'success',
      activate
        ? config.requiresApproval
          ? 'Submitted for approval.'
          : 'Saved and activated.'
        : 'Draft saved.'
    );
    setTimeout(() => onBack(), 800);
  };

  const handleDeactivate = () => {
    if (!form.id) return;
    if (!window.confirm(`Deactivate "${form.recordName ?? form.name ?? form.id}"?`)) return;
    onDeactivate(form);
    pushToast('warning', 'Record marked Inactive.');
    setTimeout(() => onBack(), 600);
  };

  const codeValue = String(form[codeKey] ?? form.recordCode ?? form.code ?? '');
  const nameValue = String(form[nameKey] ?? form.recordName ?? form.name ?? '');

  return (
    <div
      style={{ minHeight: '100vh', background: PAGE_BG, display: 'flex', flexDirection: 'column' }}
    >
      {/* Sticky topbar */}
      <div
        style={{
          position: 'sticky',
          top: 0,
          zIndex: 20,
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'space-between',
          padding: '12px 24px',
          background: '#FFFFFF',
          borderBottom: `0.5px solid ${FOG}`,
        }}
      >
        <div style={{ display: 'flex', alignItems: 'center', gap: 12 }}>
          <button
            type="button"
            onClick={onBack}
            style={{
              height: 30,
              padding: '0 10px',
              background: 'transparent',
              border: 'none',
              color: MUTED,
              fontSize: 12,
              cursor: 'pointer',
              display: 'inline-flex',
              alignItems: 'center',
              gap: 6,
            }}
          >
            <ArrowLeft size={14} /> Back
          </button>
          <div
            style={{ display: 'flex', alignItems: 'center', gap: 6, fontSize: 12, color: MUTED }}
          >
            <span>Masters</span>
            <ChevronRight size={12} />
            <span>{config.title}</span>
            <ChevronRight size={12} />
            <span style={{ color: INK }}>{isAdd ? 'New' : isEdit ? 'Edit' : 'View'}</span>
          </div>
        </div>
        <AutoSavePill state={saveState} apiPath={apiPath} editing={isEdit} />
      </div>

      {/* Form hero */}
      <div
        style={{
          padding: '20px 24px 16px',
          background: '#FFFFFF',
          borderBottom: `0.5px solid ${FOG}`,
          display: 'flex',
          alignItems: 'flex-start',
          justifyContent: 'space-between',
          gap: 16,
        }}
      >
        <div style={{ display: 'flex', alignItems: 'center', gap: 14 }}>
          <div
            style={{
              width: 44,
              height: 44,
              borderRadius: 10,
              background: TEAL_TINT,
              color: TEAL_DARK,
              display: 'inline-flex',
              alignItems: 'center',
              justifyContent: 'center',
            }}
          >
            <Icon size={22} />
          </div>
          <div>
            <h1 style={{ fontSize: 17, fontWeight: 500, color: INK, margin: 0 }}>
              {isAdd
                ? `New ${config.title.replace(/ Master$/i, '')}`
                : nameValue || codeValue || config.title}
            </h1>
            <p style={{ fontSize: 12, color: MUTED, margin: '4px 0 0 0' }}>
              {isAdd
                ? 'Fill in the details below. Auto-save activates on first save.'
                : `${config.title} · ${codeValue || '—'}`}
            </p>
          </div>
        </div>
        <StatusBadge status={String(form.status ?? 'Active')} />
      </div>

      {/* Body — single card with section dividers */}
      <div style={{ flex: 1, padding: '20px 24px 96px' }}>
        {/* Toasts (top of form, stacked, max 3) */}
        {toasts.length > 0 && (
          <div style={{ marginBottom: 12, display: 'flex', flexDirection: 'column', gap: 6 }}>
            {toasts.map((t) => (
              <ToastBanner key={t.id} toast={t} onDismiss={() => dismissToast(t.id)} />
            ))}
          </div>
        )}

        <div
          style={{
            background: '#FFFFFF',
            border: `0.5px solid ${FOG}`,
            borderRadius: CARD_RADIUS,
            overflow: 'hidden',
          }}
        >
          {/* Universal section — Code + Name always first */}
          <FormSection
            title="Identification"
            subtitle="Code is auto-generated; name identifies the record across the system."
            stripe="teal"
          >
            <FieldRow
              label="Code"
              required
              error={errors['__code']}
              touched={touched.has('__code')}
              autoGenerated
            >
              <ReadOnlyInput
                value={codeValue}
                placeholder={isAdd ? 'Auto-generated on save' : ''}
                ariaLabel="Code (auto-generated)"
              />
              <AutoPill />
            </FieldRow>
            {!config.suppressUniversalName && (
              <FieldRow
                label="Name"
                required
                error={touched.has('__name') ? errors['__name'] : undefined}
                touched={touched.has('__name')}
              >
                <input
                  type="text"
                  value={nameValue}
                  onChange={(e) => {
                    writeTop(nameKey, e.target.value);
                    writeTop('recordName', e.target.value);
                    writeTop('name', e.target.value);
                  }}
                  onBlur={() => {
                    setTouched((cur) => new Set(cur).add('__name'));
                    setErrors((cur) => {
                      const next = { ...cur };
                      if (!String(nameValue).trim()) next['__name'] = 'Name is required';
                      else delete next['__name'];
                      return next;
                    });
                  }}
                  readOnly={readOnly}
                  placeholder="Display name"
                  style={inputStyle(touched.has('__name'), errors['__name'], readOnly)}
                />
                {!errors['__name'] && touched.has('__name') && <ValidIcon />}
              </FieldRow>
            )}
          </FormSection>

          {/* Configured sections */}
          {config.formSections.map((section) => (
            <FormSection
              key={section.id}
              title={section.title}
              subtitle={section.subtitle}
              stripe={section.stripe ?? 'blue'}
              fullWidth={Boolean(section.customRender)}
            >
              {section.customRender
                ? section.customRender({ form, setForm, readOnly })
                : section.fields.map((field) => (
                    <ConfiguredFieldRow
                      key={field.key}
                      field={field}
                      form={form}
                      setForm={setForm}
                      touched={touched.has(field.key)}
                      error={errors[field.key]}
                      readOnly={readOnly}
                      onChange={(v) => writeField(field, v)}
                      onBlur={() => onFieldBlur(field)}
                    />
                  ))}
            </FormSection>
          ))}

          {/* Entity mapping (only when config.entityScoped). Re-uses the
              existing EntityMappingSelector — global vs per-entity scope. */}
          {config.entityScoped && (
            <div style={{ borderTop: `0.5px solid ${FOG}`, padding: '16px 20px 20px' }}>
              <EntityMappingSelector
                value={(form.entityMappings as EntityScopeMapping[] | undefined) ?? []}
                onChange={(mappings) => setForm((prev) => ({ ...prev, entityMappings: mappings }))}
              />
            </div>
          )}

          {/* Always-on bottom section — status / approval */}
          <FormSection title="Status" subtitle="Activation and approval state." stripe="amber">
            <FieldRow label="Status">
              <select
                value={String(form.status ?? 'Active')}
                onChange={(e) => writeTop('status', e.target.value)}
                disabled={readOnly}
                style={inputStyle(false, undefined, readOnly)}
              >
                <option value="Active">Active</option>
                <option value="Inactive">Inactive</option>
              </select>
            </FieldRow>
            <FieldRow label="Approval">
              <select
                value={String(form.approvalStatus ?? 'Draft')}
                onChange={(e) => writeTop('approvalStatus', e.target.value)}
                disabled={readOnly}
                style={inputStyle(false, undefined, readOnly)}
              >
                <option value="Draft">Draft</option>
                <option value="Pending Approval">Pending Approval</option>
                <option value="Approved">Approved</option>
                <option value="Rejected">Rejected</option>
              </select>
            </FieldRow>
          </FormSection>
        </div>
      </div>

      {/* Sticky footer */}
      <div
        style={{
          position: 'sticky',
          bottom: 0,
          zIndex: 10,
          display: 'flex',
          alignItems: 'center',
          justifyContent: 'space-between',
          padding: '12px 24px',
          background: '#FFFFFF',
          borderTop: `0.5px solid ${FOG}`,
          boxShadow: '0 -4px 12px rgba(0,0,0,0.04)',
        }}
      >
        <div style={{ fontSize: 11, color: MUTED }}>
          {lastSavedAt ? `Last saved ${formatRelative(lastSavedAt)}` : 'Not saved yet'}
        </div>
        <div style={{ display: 'flex', gap: 8, alignItems: 'center' }}>
          {isEdit && String(form.status ?? '').toLowerCase() === 'active' && (
            <button type="button" style={ghostRedBtn} onClick={handleDeactivate}>
              Deactivate
            </button>
          )}
          <button type="button" style={secondaryBtn} onClick={onBack}>
            Cancel
          </button>
          {isView && config.requiresApproval && form.approvalStatus === 'Pending Approval' && (
            <>
              <button
                type="button"
                style={ghostRedBtn}
                onClick={async () => {
                  const comments = window.prompt('Reason for rejection (optional)') ?? '';
                  await onApprovalAction(form, 'reject', comments);
                  pushToast('warning', 'Record rejected.');
                  setTimeout(() => onBack(), 600);
                }}
              >
                <ThumbsDown size={14} /> Reject
              </button>
              <button
                type="button"
                style={primaryBtn}
                onClick={async () => {
                  await onApprovalAction(form, 'approve');
                  pushToast('success', 'Record approved.');
                  setTimeout(() => onBack(), 600);
                }}
              >
                <ThumbsUp size={14} /> Approve
              </button>
            </>
          )}
          {!isView && (
            <>
              <button type="button" style={secondaryBtn} onClick={() => submit(false)}>
                Save draft
              </button>
              <button type="button" style={primaryBtn} onClick={() => submit(true)}>
                <CheckCircle2 size={14} />{' '}
                {config.requiresApproval ? 'Save & submit for approval' : 'Save & activate'}
              </button>
            </>
          )}
        </div>
      </div>
    </div>
  );
}

// ── Audit view ─────────────────────────────────────────────────────────────

function AuditView({
  config,
  apiPath,
  codeKey,
  nameKey,
  record,
  onBack,
}: {
  config: MasterV2Config;
  apiPath: string;
  codeKey: string;
  nameKey: string;
  record: MasterV2Record;
  onBack: () => void;
}) {
  const Icon = config.icon;
  // Without a per-master audit-log endpoint, the trail is derived from the
  // record's own metadata: created → updated → current state. Future server
  // work can expand this into a real change-log fetch.
  const entries = useMemo(() => {
    const items: Array<{
      tone: 'teal' | 'blue' | 'amber' | 'grey';
      action: string;
      user: string;
      at: string;
    }> = [];
    const recordName = String(record[nameKey] ?? record.recordName ?? record.name ?? record.id);
    if (record.createdAt) {
      items.push({
        tone: 'teal',
        action: `Record created — ${recordName}`,
        user: String(record.createdBy ?? record.created_by ?? 'System'),
        at: String(record.createdAt),
      });
    }
    if (record.updatedAt && record.updatedAt !== record.createdAt) {
      items.push({
        tone: 'blue',
        action: 'Record updated',
        user: String(record.updatedBy ?? record.updated_by ?? 'System'),
        at: String(record.updatedAt),
      });
    }
    const approval = String(record.approvalStatus ?? '');
    if (approval && approval !== 'Draft') {
      items.push({
        tone: approval === 'Approved' ? 'teal' : approval === 'Rejected' ? 'amber' : 'blue',
        action: `Approval state: ${approval}`,
        user: String(record.approvedBy ?? record.approved_by ?? 'System'),
        at: String(record.approvedAt ?? record.approved_at ?? record.updatedAt ?? ''),
      });
    }
    return items;
  }, [record]);

  return (
    <div
      style={{ minHeight: '100vh', background: PAGE_BG, display: 'flex', flexDirection: 'column' }}
    >
      <div
        style={{
          position: 'sticky',
          top: 0,
          zIndex: 20,
          display: 'flex',
          alignItems: 'center',
          gap: 12,
          padding: '12px 24px',
          background: '#FFFFFF',
          borderBottom: `0.5px solid ${FOG}`,
        }}
      >
        <button
          type="button"
          onClick={onBack}
          style={{
            height: 30,
            padding: '0 10px',
            background: 'transparent',
            border: 'none',
            color: MUTED,
            fontSize: 12,
            cursor: 'pointer',
            display: 'inline-flex',
            alignItems: 'center',
            gap: 6,
          }}
        >
          <ArrowLeft size={14} /> Back
        </button>
        <div style={{ display: 'flex', alignItems: 'center', gap: 6, fontSize: 12, color: MUTED }}>
          <span>Masters</span>
          <ChevronRight size={12} />
          <span>{config.title}</span>
          <ChevronRight size={12} />
          <span style={{ color: INK }}>Audit trail</span>
        </div>
      </div>

      <div
        style={{
          padding: '20px 24px 16px',
          background: '#FFFFFF',
          borderBottom: `0.5px solid ${FOG}`,
          display: 'flex',
          alignItems: 'center',
          gap: 14,
        }}
      >
        <div
          style={{
            width: 44,
            height: 44,
            borderRadius: 10,
            background: TEAL_TINT,
            color: TEAL_DARK,
            display: 'inline-flex',
            alignItems: 'center',
            justifyContent: 'center',
          }}
        >
          <Icon size={22} />
        </div>
        <div>
          <h1 style={{ fontSize: 17, fontWeight: 500, color: INK, margin: 0 }}>
            {String(record[nameKey] ?? record.recordName ?? record.name ?? record.id ?? '—')}
          </h1>
          <p style={{ fontSize: 12, color: MUTED, margin: '4px 0 0 0' }}>
            {apiPath} · {String(record[codeKey] ?? record.recordCode ?? record.code ?? '—')}
          </p>
        </div>
      </div>

      <div style={{ flex: 1, padding: '20px 24px' }}>
        <div
          style={{
            background: '#FFFFFF',
            border: `0.5px solid ${FOG}`,
            borderRadius: CARD_RADIUS,
            padding: 20,
          }}
        >
          {entries.length === 0 ? (
            <p style={{ fontSize: 12, color: MUTED, margin: 0 }}>
              No history available yet for this record.
            </p>
          ) : (
            <ol
              style={{
                margin: 0,
                padding: 0,
                listStyle: 'none',
                display: 'flex',
                flexDirection: 'column',
                gap: 12,
              }}
            >
              {entries.map((e, i) => (
                <li
                  key={i}
                  style={{
                    display: 'flex',
                    gap: 12,
                    alignItems: 'flex-start',
                    paddingBottom: 12,
                    borderBottom: i < entries.length - 1 ? `0.5px solid ${FOG}` : 'none',
                  }}
                >
                  <span
                    style={{
                      width: 30,
                      height: 30,
                      borderRadius: 8,
                      background:
                        e.tone === 'teal'
                          ? TEAL_TINT
                          : e.tone === 'blue'
                            ? '#E6F1FB'
                            : e.tone === 'amber'
                              ? '#FAEEDA'
                              : '#F4F7FB',
                      color:
                        e.tone === 'teal'
                          ? TEAL_DARK
                          : e.tone === 'blue'
                            ? '#0C447C'
                            : e.tone === 'amber'
                              ? '#633806'
                              : '#516173',
                      display: 'inline-flex',
                      alignItems: 'center',
                      justifyContent: 'center',
                      flexShrink: 0,
                    }}
                  >
                    <History size={14} />
                  </span>
                  <div style={{ flex: 1 }}>
                    <div style={{ fontSize: 13, fontWeight: 500, color: INK }}>{e.action}</div>
                    <div style={{ fontSize: 11, color: MUTED, marginTop: 2 }}>
                      {e.user} · {formatRelative(e.at ? new Date(e.at) : new Date())}
                    </div>
                  </div>
                </li>
              ))}
            </ol>
          )}
        </div>
      </div>
    </div>
  );
}

// ── Small visual helpers ───────────────────────────────────────────────────

function FormSection({
  title,
  subtitle,
  stripe = 'teal',
  fullWidth = false,
  children,
}: {
  title: string;
  subtitle?: string;
  stripe?: SectionStripe;
  fullWidth?: boolean;
  children: ReactNode;
}) {
  const stripeColor = stripe === 'teal' ? TEAL : stripe === 'blue' ? STRIPE_BLUE : STRIPE_AMBER;
  return (
    <div style={{ borderTop: `0.5px solid ${FOG}` }}>
      <div
        style={{
          display: 'flex',
          alignItems: 'flex-start',
          gap: 10,
          padding: '16px 20px 12px',
          borderLeft: `3px solid ${stripeColor}`,
          marginLeft: -1,
        }}
      >
        <div>
          <div style={{ fontSize: 13, fontWeight: 600, color: INK }}>{title}</div>
          {subtitle && <div style={{ fontSize: 11, color: MUTED, marginTop: 2 }}>{subtitle}</div>}
        </div>
      </div>
      <div
        style={
          fullWidth
            ? { padding: '0 20px 20px' }
            : {
                display: 'grid',
                gridTemplateColumns: '1fr 1fr',
                gap: 16,
                padding: '0 20px 20px',
              }
        }
      >
        {children}
      </div>
    </div>
  );
}

function FieldRow({
  label,
  required,
  error,
  touched,
  autoGenerated,
  children,
}: {
  label: string;
  required?: boolean;
  error?: string;
  touched?: boolean;
  autoGenerated?: boolean;
  children: ReactNode;
}) {
  return (
    <div>
      <label
        style={{
          display: 'block',
          fontSize: 11,
          color: MUTED,
          marginBottom: 6,
          fontWeight: 500,
        }}
      >
        {label}
        {required && <span style={{ color: ERROR_RED, marginLeft: 4 }}>*</span>}
        {autoGenerated && (
          <span style={{ color: MUTED, marginLeft: 6, fontSize: 10 }}>(system-generated)</span>
        )}
      </label>
      <div style={{ position: 'relative', display: 'flex', alignItems: 'center', gap: 6 }}>
        {children}
      </div>
      {error && touched && (
        <div
          style={{
            marginTop: 4,
            fontSize: 11,
            color: ERROR_RED,
            display: 'flex',
            alignItems: 'center',
            gap: 4,
          }}
        >
          <AlertCircle size={11} /> {error}
        </div>
      )}
    </div>
  );
}

function ConfiguredFieldRow({
  field,
  form,
  touched,
  error,
  readOnly,
  onChange,
  onBlur,
  setForm,
}: {
  field: MasterV2Field;
  form: MasterV2Record;
  touched: boolean;
  error?: string;
  readOnly: boolean;
  onChange: (v: unknown) => void;
  onBlur: () => void;
  setForm: (updater: (prev: MasterV2Record) => MasterV2Record) => void;
}) {
  const value = readField(form, field);
  const stringValue = value == null ? '' : String(value);
  const isAuto = field.autoGenerated === true;
  const effectiveReadOnly = readOnly || isAuto;

  if (isAuto) {
    return (
      <FieldRow label={field.label} autoGenerated>
        <ReadOnlyInput
          value={stringValue}
          placeholder={stringValue ? '' : 'Auto-generated on save'}
          ariaLabel={`${field.label} (auto-generated)`}
        />
        <AutoPill />
      </FieldRow>
    );
  }

  let control: ReactNode;
  if (field.type === 'select' && field.dynamicOptions) {
    const handleDynamicChange = (newId: string) => {
      const matched = field.dynamicOptions?.find((o) => o.value === newId);
      const label = matched?.label ?? '';
      if (field.labelKey) {
        setForm((prev) => ({ ...prev, [field.key]: newId, [field.labelKey as string]: label }));
      } else {
        onChange(newId);
      }
    };
    control = (
      <select
        value={stringValue}
        onChange={(e) => handleDynamicChange(e.target.value)}
        onBlur={onBlur}
        disabled={effectiveReadOnly}
        style={inputStyle(touched, error, effectiveReadOnly)}
      >
        <option value="">Select…</option>
        {field.dynamicOptions.map((o) => (
          <option key={o.value} value={o.value}>
            {o.label}
          </option>
        ))}
      </select>
    );
  } else if (field.type === 'select' && field.options) {
    control = (
      <select
        value={stringValue}
        onChange={(e) => onChange(e.target.value)}
        onBlur={onBlur}
        disabled={effectiveReadOnly}
        style={inputStyle(touched, error, effectiveReadOnly)}
      >
        <option value="">Select…</option>
        {field.options.map((o) => (
          <option key={o} value={o}>
            {o}
          </option>
        ))}
      </select>
    );
  } else if (field.type === 'textarea') {
    control = (
      <textarea
        value={stringValue}
        onChange={(e) => onChange(e.target.value)}
        onBlur={onBlur}
        readOnly={effectiveReadOnly}
        placeholder={field.placeholder}
        rows={2}
        style={{
          ...inputStyle(touched, error, effectiveReadOnly),
          minHeight: 64,
          resize: 'vertical',
        }}
      />
    );
  } else {
    control = (
      <input
        type={
          field.type === 'date'
            ? 'date'
            : field.type === 'number' || field.type === 'currency'
              ? 'number'
              : 'text'
        }
        value={stringValue}
        onChange={(e) => onChange(e.target.value)}
        onBlur={onBlur}
        readOnly={effectiveReadOnly}
        placeholder={field.placeholder}
        style={inputStyle(touched, error, effectiveReadOnly)}
      />
    );
  }

  return (
    <FieldRow label={field.label} required={field.required} error={error} touched={touched}>
      {control}
      {!error && touched && stringValue && <ValidIcon />}
    </FieldRow>
  );
}

