function resolveCell(record: MasterV2Record, col: MasterV2Column): unknown {
  if (col.fromPayload) {
    const payload = (record.payload as Record<string, unknown>) ?? {};
    return payload[col.key];
  }
  return record[col.key];
}

function readField(record: MasterV2Record, field: MasterV2Field): unknown {
  if (field.fromPayload) {
    const payload = (record.payload as Record<string, unknown>) ?? {};
    return payload[field.key];
  }
  return record[field.key];
}

function formatRelative(d: Date): string {
  const now = Date.now();
  const diff = now - d.getTime();
  if (diff < 0) return d.toLocaleString('en-IN');
  if (diff < 60 * 1000) return 'just now';
  if (diff < 60 * 60 * 1000) return `${Math.floor(diff / 60000)} min ago`;
  if (diff < 24 * 60 * 60 * 1000) return `${Math.floor(diff / 3600000)} hr ago`;
  return d.toLocaleDateString('en-IN', { day: '2-digit', month: 'short', year: 'numeric' });
}
