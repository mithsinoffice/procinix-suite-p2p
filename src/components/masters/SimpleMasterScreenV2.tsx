// ── Public component ───────────────────────────────────────────────────────

export function SimpleMasterScreenV2({ config }: { config: MasterV2Config }) {
  const [records, setRecords, isHydrating] = useIncrementalMasterRecords<MasterV2Record>(
    config.masterKey,
    []
  );

  const [viewMode, setViewMode] = useState<ViewMode>('list');
  const [selectedRecord, setSelectedRecord] = useState<MasterV2Record | null>(null);
  const [searchTerm, setSearchTerm] = useState('');
  const [statusFilter, setStatusFilter] = useState<'all' | 'Active' | 'Inactive'>('all');
  const [page, setPage] = useState(0);
  const [pageSize, setPageSize] = useState<10 | 25 | 50>(10);

  const apiPath = config.apiPath ?? `/masters/${config.masterKey}`;
  const codeKey = config.codeKey ?? 'recordCode';
  const nameKey = config.nameKey ?? 'recordName';

  const readCode = useCallback(
    (r: MasterV2Record): string => String(r[codeKey] ?? r.recordCode ?? r.code ?? ''),
    [codeKey]
  );
  const readName = useCallback(
    (r: MasterV2Record): string => String(r[nameKey] ?? r.recordName ?? r.name ?? ''),
    [nameKey]
  );

  // ── Listing filters ──────────────────────────────────────────────────────
  const filtered = useMemo(() => {
    const q = searchTerm.trim().toLowerCase();
    return records.filter((r) => {
      if (
        statusFilter !== 'all' &&
        String(r.status ?? '').toLowerCase() !== statusFilter.toLowerCase()
      ) {
        return false;
      }
      if (!q) return true;
      return readCode(r).toLowerCase().includes(q) || readName(r).toLowerCase().includes(q);
    });
  }, [records, searchTerm, statusFilter, readCode, readName]);

  const totalPages = Math.max(1, Math.ceil(filtered.length / pageSize));
  const pageRows = filtered.slice(page * pageSize, page * pageSize + pageSize);

  useEffect(() => {
    if (page > totalPages - 1) setPage(0);
  }, [totalPages, page]);

  // ── Navigation between views ─────────────────────────────────────────────
  const openAdd = useCallback(() => {
    setSelectedRecord({ status: 'Active', approvalStatus: 'Draft', payload: {} });
    setViewMode('add');
  }, []);
  const openEdit = useCallback((r: MasterV2Record) => {
    setSelectedRecord(r);
    setViewMode('edit');
  }, []);
  const openView = useCallback((r: MasterV2Record) => {
    setSelectedRecord(r);
    setViewMode('view');
  }, []);
  const openAudit = useCallback((r: MasterV2Record) => {
    setSelectedRecord(r);
    setViewMode('audit');
  }, []);
  const backToList = useCallback(() => {
    setSelectedRecord(null);
    setViewMode('list');
  }, []);

  const handleSave = useCallback(
    (next: MasterV2Record): MasterV2Record => {
      const code = readCode(next);
      const name = readName(next);
      const id = String(next.id ?? code ?? `R-${Date.now()}`);
      const stamped: MasterV2Record = {
        ...next,
        id,
        [codeKey]: code,
        // Maintain the universal recordCode/recordName mirrors so toolbar /
        // listing utilities that don't read the custom keys keep working.
        recordCode: code,
        code,
        [nameKey]: name,
        recordName: name,
        name,
        updatedAt: new Date().toISOString(),
        createdAt: next.createdAt ?? new Date().toISOString(),
      };
      setRecords((current) => {
        const existingIx = current.findIndex((r) => r.id === id);
        if (existingIx >= 0) {
          return current.map((r, i) => (i === existingIx ? stamped : r));
        }
        return [...current, stamped];
      });
      return stamped;
    },
    [setRecords, codeKey, nameKey, readCode, readName]
  );

  const handleDeactivate = useCallback(
    (r: MasterV2Record) => {
      if (!r.id) return;
      setRecords((current) =>
        current.map((row) =>
          row.id === r.id
            ? { ...row, status: 'Inactive', updatedAt: new Date().toISOString() }
            : row
        )
      );
    },
    [setRecords]
  );

  const handleApprovalAction = useCallback(
    async (r: MasterV2Record, action: 'approve' | 'reject', comments = '') => {
      if (!r.id) return;
      try {
        const next = await applyMasterApprovalAction<MasterV2Record>(
          config.masterKey,
          records,
          r.id,
          action,
          comments
        );
        setRecords(next);
      } catch (err) {
        console.error(`[SimpleMasterScreenV2] ${action} failed:`, err);
      }
    },
    [config.masterKey, records, setRecords]
  );

  // ── Render branch ────────────────────────────────────────────────────────
  if (viewMode === 'list') {
    return (
      <ListingView
        config={config}
        apiPath={apiPath}
        codeKey={codeKey}
        nameKey={nameKey}
        records={records}
        filtered={filtered}
        pageRows={pageRows}
        isHydrating={isHydrating}
        searchTerm={searchTerm}
        onSearch={setSearchTerm}
        statusFilter={statusFilter}
        onStatusFilter={setStatusFilter}
        page={page}
        pageSize={pageSize}
        totalPages={totalPages}
        onPageChange={setPage}
        onPageSizeChange={(s) => {
          setPageSize(s);
          setPage(0);
        }}
        onAdd={openAdd}
        onView={openView}
        onEdit={openEdit}
        onAudit={openAudit}
      />
    );
  }

  if (viewMode === 'audit' && selectedRecord) {
    return (
      <AuditView
        config={config}
        apiPath={apiPath}
        codeKey={codeKey}
        nameKey={nameKey}
        record={selectedRecord}
        onBack={backToList}
      />
    );
  }

  if (selectedRecord && (viewMode === 'add' || viewMode === 'edit' || viewMode === 'view')) {
    return (
      <FormView
        key={selectedRecord.id ?? 'new'}
        config={config}
        apiPath={apiPath}
        codeKey={codeKey}
        nameKey={nameKey}
        mode={viewMode}
        initial={selectedRecord}
        onBack={backToList}
        onSave={handleSave}
        onDeactivate={handleDeactivate}
        onApprovalAction={handleApprovalAction}
      />
    );
  }

  return null;
}

